public class Calculator {

    public int addition(int int1, int int2){
        return int1 + int2;
    }

    public int subtraction(int int1, int int2){
        return int1 - int2;
    }

    public int multiplication(int int1, int int2){
        return int1 * int2;
    }

    public double division(double int1, double int2){
        return int1 / int2;
    }
}
